Q1_TITLE = "一、分类型机器学习算法"

Q1_BODY = """
分类型机器学习是监督学习的一个重要分支，其目标是根据已标注的训练数据学习一个从特征空间到离散类别标签的映射函数，从而对未知样本的类别进行预测。在量化交易领域，分类算法被广泛应用于预测股票涨跌方向、信用违约风险识别、市场状态分类等场景。本任务重点介绍三种经典的分类算法：逻辑回归、决策树和随机森林。

逻辑回归（Logistic Regression）虽然名称中包含"回归"二字，但实际上是一种广泛使用的二分类算法。其核心思想是通过Sigmoid函数将线性回归的输出映射到(0,1)区间，表示样本属于正类的概率。Sigmoid函数的数学形式为σ(z)=1/(1+e^(-z))，其中z=w^T x+b是特征的线性组合。逻辑回归通过最大似然估计来优化模型参数，使得模型对训练数据的预测概率尽可能接近真实标签。逻辑回归的优点在于模型简单、训练速度快、可解释性强（权重反映特征重要性），且输出的概率值可以直接用于排序和决策。其局限性在于只能处理线性可分的问题，对于复杂的非线性决策边界需要通过特征工程或引入多项式特征来扩展。在量化交易中，逻辑回归常用于构建因子选股模型和风险评分模型。

决策树（Decision Tree）是一种基于树形结构进行决策的非参数分类算法。决策树通过递归地选择最优特征和分裂点，将特征空间划分为若干个互不重叠的区域，每个叶节点对应一个类别标签。决策树的构建过程包括特征选择、决策树生成和剪枝三个步骤。特征选择常用的准则有信息增益（ID3算法）、信息增益比（C4.5算法）和基尼指数（CART算法）。决策树的优点在于：模型直观易懂，可以直接转化为if-then规则；能够处理数值型和类别型特征；不需要特征标准化；能够捕捉特征间的非线性关系和交互效应。其缺点是容易过拟合，特别是当树的深度较大时，模型会对训练数据中的噪声过于敏感。通过限制树的深度（max_depth）或进行剪枝可以缓解过拟合问题。

随机森林（Random Forest）是一种基于决策树的集成学习方法。其核心思想是通过构建多棵决策树并对它们的预测结果进行投票（分类）或平均（回归），来提升模型的泛化能力和稳定性。随机森林在构建每棵树时引入了两层随机性：第一，对训练数据进行Bootstrap采样（有放回抽样），每棵树使用不同的训练子集；第二，在每个节点分裂时，从全部特征中随机选取一个子集作为候选特征，只在子集中选择最优分裂特征。这两层随机性使得各棵树之间具有足够的多样性，从而降低了集成的方差。随机森林的优点包括：预测精度高，通常优于单棵决策树；对过拟合有天然的抵抗能力；能够评估特征重要性；对缺失值和异常值具有较好的鲁棒性；可并行化训练。其缺点是模型可解释性不如单棵决策树，且预测速度较慢（需要多棵树投票）。在量化交易中，随机森林被广泛应用于因子挖掘、多因子选股模型和市场趋势预测等场景。

综上所述，逻辑回归适合处理特征与标签之间关系近似线性的场景，决策树适合需要强可解释性的场景，随机森林适合追求高精度且不需要强解释性的场景。在实际应用中，通常会同时训练多个模型并通过交叉验证选择最优模型。
""".strip()


Q2_TITLE = "二、机器学习模型评价指标"

Q2_BODY = """
机器学习模型的性能评估是模型开发过程中至关重要的一环。选择合适的评价指标不仅能够客观衡量模型的质量，还能指导模型的优化方向。本任务重点介绍混淆矩阵、AUC和ROC曲线三个核心评估概念。

混淆矩阵（Confusion Matrix）是分类模型评估的基础工具，它以矩阵的形式展示了模型预测结果与真实标签之间的对应关系。对于二分类问题，混淆矩阵是一个2×2的矩阵，包含四个基本元素：真正例（True Positive, TP）表示实际为正类且预测为正类的样本数；假正例（False Positive, FP）表示实际为负类但预测为正类的样本数；假负例（False Negative, FN）表示实际为正类但预测为负类的样本数；真负例（True Negative, TN）表示实际为负类且预测为负类的样本数。基于混淆矩阵可以衍生出多个评估指标：准确率（Accuracy）=(TP+TN)/(TP+TN+FP+FN)，衡量整体预测正确率；精确率（Precision）=TP/(TP+FP)，衡量预测为正类的样本中真正为正类的比例；召回率（Recall）=TP/(TP+FN)，衡量实际为正类的样本中被正确预测的比例；F1分数=2×Precision×Recall/(Precision+Recall)，是精确率和召回率的调和平均值。在量化交易中，召回率对应"在所有实际上涨的股票中，模型正确预测上涨的比例"，精确率对应"在模型预测上涨的股票中，实际上涨的比例"。

ROC曲线（Receiver Operating Characteristic Curve）是以假正率（FPR=FP/(FP+TN)）为横轴、真正率（TPR=TP/(TP+FN)，即召回率）为纵轴绘制的曲线。ROC曲线通过遍历分类器的所有可能阈值，在每个阈值下计算对应的FPR和TPR，连接所有点形成曲线。ROC曲线直观地展示了模型在不同决策阈值下"召回率"与"误报率"之间的权衡关系。曲线越接近左上角(0,1)点，表示模型性能越好——即在保持低误报率的同时实现高召回率。对角线y=x表示随机分类器的性能，如果ROC曲线低于对角线，说明模型性能不如随机猜测。ROC曲线的优点在于它不依赖于具体的分类阈值，能够全面评估模型在不同工作点下的性能，且对类别不平衡问题不敏感。

AUC（Area Under Curve）是ROC曲线下的面积，取值范围为[0,1]。AUC将ROC曲线的信息压缩为一个标量值，便于模型间的直接比较。AUC的物理含义是：随机选取一个正类样本和一个负类样本，模型将正类样本的预测概率排在负类样本之前的概率。AUC=1表示完美分类器，AUC=0.5表示随机分类器，AUC<0.5表示模型反向预测（比随机还差）。一般而言，AUC>0.7表示模型具有较好的区分能力，AUC>0.8表示模型性能良好，AUC>0.9表示模型性能优秀。AUC作为评价指标的优点在于：它不依赖于分类阈值的选择，能够综合反映模型的整体排序能力；对类别不平衡数据具有鲁棒性；计算简便且可解释性强。在量化交易中，AUC常用于评估选股模型的排序能力——AUC越高，说明模型对上涨股票和下跌股票的区分能力越强。

在实际应用中，混淆矩阵提供了最基础的分类结果统计，ROC曲线展示了模型在不同阈值下的权衡关系，AUC提供了模型整体性能的量化指标。三者结合使用，可以全面评估分类模型的性能，为模型选择和优化提供依据。
""".strip()


