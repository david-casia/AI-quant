Q1_TITLE = "一、双均线策略原理"
Q1_BODY = """
双均线策略（Dual Moving Average Strategy）是量化交易领域最经典、应用最广泛的趋势跟踪策略之一。其核心思想是利用两条不同周期的移动平均线的交叉关系来判断市场趋势的方向变化，并据此产生交易信号。移动平均线（Moving Average，简称MA）是对过去若干个交易日收盘价取算术平均值后连成的曲线，能够有效平滑价格波动，揭示价格运行的潜在趋势。

在双均线策略中，通常选取一条短期均线（如5日均线）和一条长期均线（如15日均线）。短期均线对价格变化反应灵敏，能够快速捕捉近期价格走势；长期均线则更为平缓，反映的是较长时间周期内的价格中枢。两条均线的相对位置关系构成了策略的核心判断依据。

金叉（Golden Cross）是指短期均线从下方穿越长期均线向上突破的现象。当短期均线上穿长期均线时，意味着近期价格走势已经强于长期平均水平，市场可能正在形成上升趋势，此时产生买入信号。金叉通常被视为多头行情的起始标志，表明短期内买方力量开始占据主导地位。

死叉（Death Cross）是指短期均线从上方穿越长期均线向下跌破的现象。当短期均线下穿长期均线时，意味着近期价格走势已经弱于长期平均水平，市场可能正在形成下降趋势，此时产生卖出信号。死叉通常被视为空头行情的起始标志，表明短期内卖方力量开始占据主导地位。

双均线策略的理论基础源于技术分析中的趋势跟踪理念。该理念认为，市场价格一旦形成某种趋势，便具有较强的惯性，趋势的延续概率大于反转概率。通过均线的交叉信号，投资者可以在趋势形成的早期阶段介入，在趋势结束的早期阶段退出，从而获取趋势波段的大部分利润。然而，双均线策略在震荡市中容易产生频繁的虚假信号，导致连续的小额亏损，这是该策略的主要局限性。

在本任务中，选取5日均线作为短期均线，15日均线作为长期均线。当MA5上穿MA15时产生买入信号（金叉），执行全仓买入操作；当MA5下穿MA15时产生卖出信号（死叉），执行全仓卖出操作。通过对比策略收益与买入持有策略的收益，可以评估双均线策略的有效性。
""".strip()


Q2_TITLE = "二、量化策略绩效评估指标"
Q2_BODY = """
为了全面、客观地评估交易策略的表现，量化交易领域发展出了一系列标准化的绩效评估指标。本任务重点介绍并计算七个核心指标：年化收益率、最大回撤、年化波动率、夏普比率、胜率、盈亏比和期望收益(R)。

年化收益率（Annualized Return）是将策略在任意时间段内的累计收益率折算为以年为单位的等价收益率，便于不同时间跨度的策略之间进行横向比较。其计算公式为：年化收益率 = (期末净值)^(252/交易日数) - 1。年化收益率是衡量策略盈利能力的基准指标，但高年化收益并不必然意味着策略优秀，还需结合风险指标综合判断。

最大回撤（Maximum Drawdown，MDD）是衡量策略风险的关键指标，表示策略净值从历史最高点到后续最低点的最大跌幅。其计算过程为：首先计算每个时点的累计最大净值，然后计算当前净值与历史最大净值的差额比例，所有时点中最大的跌幅即为最大回撤。最大回撤反映了投资者在使用该策略时可能面临的最严重亏损幅度，是评估策略风险承受能力的重要依据。通常认为，最大回撤超过20%的策略风险较高，超过30%则属于高风险策略。

年化波动率（Annualized Volatility）是策略日收益率标准差经过年化处理后的值，计算公式为：年化波动率 = 日收益率标准差 × sqrt(252)。波动率衡量的是策略收益的波动幅度，反映策略收益的不确定性程度。波动率越高，说明策略收益越不稳定，风险越大。通常股票型策略的年化波动率在15%~30%之间，超过30%属于高波动策略。

夏普比率（Sharpe Ratio）是由诺贝尔经济学奖得主威廉·夏普提出的风险调整收益指标，用于衡量每承担一单位风险所获得的超额收益。其计算公式为：夏普比率 = (策略年化收益率 - 无风险利率) / 策略年化标准差。夏普比率综合考虑了收益和风险两个维度，能够更全面地评估策略的质量。一般认为，夏普比率大于1表示策略表现良好，大于2表示表现优秀，小于0则表示策略收益低于无风险利率。在本任务中，无风险年化利率取2%，年化系数取252个交易日。

胜率（Win Rate）是指在所有完整交易（一次买入到一次卖出为一轮完整交易）中，盈利交易所占的比例。胜率反映了策略信号的准确程度，高胜率意味着策略发出的交易信号更多是正确的。但胜率并非越高越好——一个胜率90%但每次只赚0.1%的策略，可能不如一个胜率40%但盈利时赚5%的策略。胜率需要与盈亏比配合使用才能完整评估策略质量。

盈亏比（Profit/Loss Ratio）是平均盈利交易的收益率与平均亏损交易的收益率（绝对值）之比。计算公式为：盈亏比 = 平均盈利 / 平均亏损。盈亏比衡量的是策略"赢的时候赢得多少"相对于"输的时候输了多少"的比例。盈亏比大于1意味着策略在盈利交易中获得的收益超过亏损交易中损失的金额。一般来说，盈亏比大于2的策略具有较好的盈利能力。盈亏比与胜率的组合关系决定了策略的整体盈利能力：高胜率+低盈亏比和高盈亏比+低胜率都可以是盈利的策略。

期望收益（Expectancy，R）是以平均亏损为单位（1R = 1倍平均亏损）衡量的每笔交易的平均期望收益。计算公式为：期望收益(R) = (胜率 × 平均盈利 - 败率 × 平均亏损) / 平均亏损。期望收益综合考虑了胜率和盈亏比两个维度，是评估策略长期盈利能力的核心指标。期望收益为正（R > 0）意味着策略长期来看能够盈利，R值越大策略盈利能力越强；期望收益为负（R < 0）则意味着策略长期会亏损。一般而言，R > 0.2R被认为是可接受的策略，R > 0.5R属于优秀策略。

上述七个指标从收益、风险、风险调整收益、交易质量和长期期望五个维度构成了策略评估的完整框架。在实际应用中，一个优秀的策略应当在获取较高年化收益的同时，保持较低的最大回撤和波动率，较高的夏普比率和正的期望收益，实现收益与风险的平衡。
""".strip()


