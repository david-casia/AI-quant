Q2_TITLE = "二、唐奇安通道、ATR与止损条件"

Q2_BODY = """
海龟策略的技术基础由三个核心组件构成：唐奇安通道、平均真实波幅（ATR）和基于ATR的止损条件。这三个组件分别负责信号生成、波动率度量和风险控制，共同构成了海龟策略的完整技术框架。

唐奇安通道（Donchian Channel）是由技术分析先驱理查德·唐奇安（Richard Donchian）发明的价格通道指标，是海龟策略信号生成的核心工具。通道由三条线构成：上轨为过去N个交易日的最高价，下轨为过去N个交易日的最低价，中轨为上下轨的均值。在海龟策略中，通常设置两个不同周期的通道：入场通道和出场通道。入场通道周期较长（如20日），用于捕捉中长期趋势的突破信号；出场通道周期较短（如10日），用于在趋势反转时及时退出。具体而言，当当日收盘价突破入场通道上轨（即创20日新高）时，产生买入信号；当当日收盘价跌破出场通道下轨（即创10日新低）时，产生卖出信号。值得注意的是，海龟法则使用"前N日"的数据计算通道（即shift(1)后取rolling），不包含当日数据，以避免未来数据泄露。唐奇安通道的优点在于计算简单、信号明确、不依赖参数拟合，能够客观反映价格波动的极端边界。

平均真实波幅（Average True Range，简称ATR）是由技术分析大师威尔斯·威尔德（Welles Wilder）发明的波动率指标，用于衡量市场价格的波动幅度。ATR的计算分为两步：首先计算每日的真实波幅（True Range, TR），TR为以下三个值中的最大值：当日最高价与最低价之差（high - low）、当日最高价与前日收盘价之差的绝对值（|high - prev_close|）、当日最低价与前日收盘价之差的绝对值（|low - prev_close|）。后两个分量考虑了跳空缺口的影响，使得TR能够更全面地反映价格的真实波动范围。然后在TR的基础上计算N日（通常为20日）的指数移动平均，即得到ATR。海龟策略使用Wilder平滑法（alpha=1/N）进行EMA计算，这与标准的EMA略有不同，但更符合原始海龟法则的定义。ATR的值越大，说明市场波动越剧烈；ATR的值越小，说明市场波动越平缓。ATR的关键特性在于它是一个动态指标，能够随市场波动率的变化而自动调整。

止损条件是海龟策略风险管理的核心机制。海龟法则使用基于ATR的动态止损策略，其核心规则为：买入后立即设置止损价为（买入价 - 止损倍数 × ATR），其中止损倍数通常取2.0。这意味着，当价格从买入价下跌超过2倍ATR时，触发止损卖出。ATR止损的精妙之处在于其自适应性：在波动率高的市场环境中，ATR值较大，止损距离相应放宽，避免因正常波动而被"洗出"；在波动率低的市场环境中，ATR值较小，止损距离收紧，及时控制损失。此外，海龟策略还采用跟踪止损（Trailing Stop）机制：持仓期间，随着价格上涨，止损价也会相应上移（仅上移不下移），从而锁定已获利润。止损价的更新公式为：新止损价 = 当日收盘价 - 止损倍数 × ATR。当新计算出的止损价高于当前止损价时，更新止损价；否则保持不变。这一机制确保了策略在趋势延续时能够保护利润，在趋势反转时能够及时退出。

以上三个组件的协同工作机制如下：唐奇安通道负责捕捉趋势突破信号，ATR负责度量当前市场的波动率水平，ATR止损负责在趋势判断失误时控制单笔交易的最大亏损。三者结合，形成了一个"入场有信号、持仓有跟踪、出场有止损"的完整交易闭环，体现了海龟策略"系统化、机械化、风控优先"的核心设计理念。
""".strip()


