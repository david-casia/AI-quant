Q1_TITLE = "一、双均线策略原理"
Q1_BODY = """
双均线策略（Dual Moving Average Strategy）是量化交易领域最经典、应用最广泛的趋势跟踪策略之一。其核心思想是利用两条不同周期的移动平均线的交叉关系来判断市场趋势的方向变化，并据此产生交易信号。移动平均线（Moving Average，简称MA）是对过去若干个交易日收盘价取算术平均值后连成的曲线，能够有效平滑价格波动，揭示价格运行的潜在趋势。

在双均线策略中，通常选取一条短期均线（如5日均线）和一条长期均线（如15日均线）。短期均线对价格变化反应灵敏，能够快速捕捉近期价格走势；长期均线则更为平缓，反映的是较长时间周期内的价格中枢。两条均线的相对位置关系构成了策略的核心判断依据。

金叉（Golden Cross）是指短期均线从下方穿越长期均线向上突破的现象。当短期均线上穿长期均线时，意味着近期价格走势已经强于长期平均水平，市场可能正在形成上升趋势，此时产生买入信号。金叉通常被视为多头行情的起始标志，表明短期内买方力量开始占据主导地位。

死叉（Death Cross）是指短期均线从上方穿越长期均线向下跌破的现象。当短期均线下穿长期均线时，意味着近期价格走势已经弱于长期平均水平，市场可能正在形成下降趋势，此时产生卖出信号。死叉通常被视为空头行情的起始标志，表明短期内卖方力量开始占据主导地位。

双均线策略的理论基础源于技术分析中的趋势跟踪理念。该理念认为，市场价格一旦形成某种趋势，便具有较强的惯性，趋势的延续概率大于反转概率。通过均线的交叉信号，投资者可以在趋势形成的早期阶段介入，在趋势结束的早期阶段退出，从而获取趋势波段的大部分利润。然而，双均线策略在震荡市中容易产生频繁的虚假信号，导致连续的小额亏损，这是该策略的主要局限性。

在本任务中，选取5日均线作为短期均线，15日均线作为长期均线。当MA5上穿MA15时产生买入信号（金叉），执行全仓买入操作；当MA5下穿MA15时产生卖出信号（死叉），执行全仓卖出操作。通过对比策略收益与买入持有策略的收益，可以评估双均线策略的有效性。
""".strip()


Q2_TITLE = "二、量化策略绩效评估指标"
Q2_BODY = """
为了全面、客观地评估交易策略的表现，量化交易领域发展出了一系列标准化的绩效评估指标。本任务重点介绍并计算三个核心指标：累计回报率、最大回撤和夏普比率。

累计回报率（Cumulative Return）是指策略在考察期内从初始资金到期末资金的总体收益率，是最直观的盈利能力衡量指标。其计算公式为：累计回报率 = (期末净值 - 1) × 100%。累计回报率反映了策略的绝对收益水平，但未考虑收益的波动性和风险因素。在评估策略时，应将其与基准收益（如买入持有策略的收益）进行对比，以判断策略的超额收益能力。

最大回撤（Maximum Drawdown，MDD）是衡量策略风险的关键指标，表示策略净值从历史最高点到 subsequent 最低点的最大跌幅。其计算过程为：首先计算每个时点的累计最大净值，然后计算当前净值与历史最大净值的差额比例，所有时点中最大的跌幅即为最大回撤。最大回撤反映了投资者在使用该策略时可能面临的最严重亏损幅度，是评估策略风险承受能力的重要依据。通常认为，最大回撤超过20%的策略风险较高，超过30%则属于高风险策略。

夏普比率（Sharpe Ratio）是由诺贝尔经济学奖得主威廉·夏普提出的风险调整收益指标，用于衡量每承担一单位风险所获得的超额收益。其计算公式为：夏普比率 = (策略年均收益率 - 无风险利率) / 策略年化标准差。夏普比率综合考虑了收益和风险两个维度，能够更全面地评估策略的质量。一般认为，夏普比率大于1表示策略表现良好，大于2表示表现优秀，小于0则表示策略收益低于无风险利率。在本任务中，无风险年化利率取2%（近似一年期国债收益率），年化系数取252个交易日。

上述三个指标从收益、风险和风险调整收益三个维度构成了策略评估的基本框架。在实际应用中，一个优秀的策略应当在获取较高累计回报的同时，保持较低的最大回撤和较高的夏普比率，实现收益与风险的平衡。
""".strip()


