# 1. 加载股价数据
df = pd.read_csv('002594_daily.csv')
df = df.sort_values('trade_date').reset_index(drop=True)

# 2. 计算短均线和长均线
df['ma_short'] = df['close'].rolling(window=5, min_periods=1).mean()
df['ma_long']  = df['close'].rolling(window=15, min_periods=1).mean()

# 3. 生成金叉死叉信号
prev_diff = df['ma_short'].shift(1) - df['ma_long'].shift(1)
curr_diff = df['ma_short'] - df['ma_long']

df.loc[(prev_diff < 0) & (curr_diff > 0), 'signal'] = 1   # 金叉买入
df.loc[(prev_diff > 0) & (curr_diff < 0), 'signal'] = -1  # 死叉卖出

# 4. 计算持仓状态
df['stock_return'] = df['close'].pct_change().fillna(0)
df['strategy_return'] = df['position'].shift(1) * df['stock_return']
df['strategy_nav'] = (1 + df['strategy_return']).cumprod()

# 5. 计算绩效指标
final_nav = df['strategy_nav'].iloc[-1]
cumulative_return = (final_nav - 1) * 100

running_max = df['strategy_nav'].cummax()
max_drawdown = ((df['strategy_nav'] - running_max) / running_max).min() * 100

excess = df['strategy_return'] - 0.02/252
sharpe = np.sqrt(252) * excess.mean() / excess.std()
