# ============ 问题二：基本概念解释 ============
Q2_TITLE = "问题二：基本概念解释——K线、基本面、技术面"

Q2_BODY = """
（一）K线

K线，又称蜡烛图（Candlestick Chart），由日本十八世纪的米市商人本间宗久所发明，是金融市场中最常用的价格图表类型之一。一根K线由四个核心价格数据构成：开盘价（Open）、收盘价（Close）、最高价（High）和最低价（Low）。

K线的图形结构分为"实体"和"影线"两部分。实体是指开盘价与收盘价之间的矩形区域，反映了一个交易周期内价格的净变动。当收盘价高于开盘价时，称为阳线（在A股惯例中通常以红色表示），表示价格上涨；当收盘价低于开盘价时，称为阴线（通常以绿色表示），表示价格下跌。实体上方和下方的细线分别称为上影线和下影线，上影线的顶端为最高价，下影线的底端为最低价，反映了该周期内价格波动的极端范围。

根据时间周期的不同，K线可分为日K线、周K线、月K线以及分钟级别的K线等。日K线反映一个交易日的价格走势，是最常用的分析周期。通过观察K线的形态组合（如十字星、锤头线、吞没形态等），交易者可以判断市场多空力量的对比和可能的价格转折点。K线分析是技术分析的基础工具之一。

（二）基本面

基本面（Fundamentals）是指影响资产内在价值的一切基本信息，包括宏观经济因素、行业状况和公司财务状况等多个层面。基本面分析的核心思想是：资产的长期价格最终取决于其内在价值，市场价格会围绕内在价值波动。

在宏观层面，基本面包括国内生产总值（GDP）增长率、通货膨胀率（CPI）、利率水平、货币政策、财政政策、汇率变动等宏观经济指标和政策环境。在行业层面，基本面包括行业景气度、市场集中度、上下游供需关系、技术变革趋势等。在公司层面，基本面包括营业收入、净利润、净资产收益率（ROE）、资产负债率、现金流状况、市盈率（PE）、市净率（PB）等财务指标，以及公司治理结构、管理层能力、竞争优势等定性因素。

基本面分析主要用于中长期投资决策，通过评估资产的内在价值与市场价格的偏离程度来判断买入或卖出时机。价值投资是基本面分析的典型代表，由本杰明·格雷厄姆和沃伦·巴菲特所倡导，强调以低于内在价值的价格买入优质资产并长期持有。

（三）技术面

技术面（Technical Analysis）是指基于市场量价历史数据，通过图表形态和技术指标来研究价格走势和趋势的分析方法。技术面分析的核心假设包括：市场行为包容消化一切（价格已反映所有信息）、价格以趋势方式演变、历史会重演。

技术面分析的工具主要包括技术指标和图表形态两大类。常见的技术指标包括移动平均线（MA）、指数移动平均线（EMA）、平滑异同移动平均线（MACD）、相对强弱指数（RSI）、随机指标（KDJ）、布林带（BOLL）等，用于衡量价格的趋势方向、动量强弱和波动范围。常见的图表形态包括头肩顶/头肩底、双顶/双底、三角形整理、旗形整理等，用于预判价格的未来走势方向。

技术面分析主要用于短期择时和交易决策，侧重于回答"何时买卖"的问题，而基本面分析则侧重于回答"买卖什么"的问题。在实际投资中，两者往往结合使用：通过基本面分析筛选标的，通过技术面分析把握进出时机。需要指出的是，技术面分析并非精确的科学预测工具，其有效性受到市场效率、参与者行为等多种因素影响，应作为辅助决策手段而非唯一依据。
""".strip()

